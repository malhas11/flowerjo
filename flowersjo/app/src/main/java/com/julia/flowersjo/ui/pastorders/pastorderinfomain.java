package com.julia.flowersjo.ui.pastorders;

public class pastorderinfomain {
    private String orderid, image, date, time, name, email, number, address, city, type;

    public pastorderinfomain(){

    }


    public pastorderinfomain(String orderid, String image, String date, String time, String name, String email, String number, String address, String city, String type ){
        this.orderid = orderid;
        this.image = image;
        this.date = date;
        this.time = time;
        this.name = name;
        this.email = email;
        this.number = number;
        this.address = address;
        this.city = city;
        this.type = type;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getOrderid() {
        return orderid;
    }

    public void setOrderid(String orderid) {
        this.orderid = orderid;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
